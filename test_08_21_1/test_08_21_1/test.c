#define _CRT_SECURE_NO_WARNINGS

#include <stdio.h>
#include "contact.h"

void menu()
{
	printf("*******************************************************\n");
	printf("**************1,add       2.del        ****************\n");
	printf("**************3.search    4.modify     ****************\n");
	printf("**************5,show      6.sort       ****************\n");
	printf("**************0.exit                   ****************\n");
	printf("*******************************************************\n");
}
int main()
{
	int input = 0;
	struct Contact con;//con就是通讯录，里边包含1000个元素的数和size
	InitContact(&con);
	do
	{
		menu();
		printf("请选择：>");
		scanf("%d", &input);
		switch (input)
		{
		case ADD:
			AddContact(&con);
			system("cls");
			break;
		case DEL:
			DelContact(&con);
			break;
		case SEARCH:
			SearchContact(&con);
			break;
		case MODIFY:
			ModifyContact(&con);
			break;
		case SHOW:
			ShowContact(&con);
			break;
			//排序
		case SORT:
			break;
		case EXIT:
			break;
		default:
			printf("选择错误，请重新选择：\n");
			break;
		}
	} while (input);
	printf("退出！！\n");
	return 0;
}